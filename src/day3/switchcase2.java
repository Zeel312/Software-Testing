package day3;

public class switchcase2 {
	public static void main(String[] args) {
		char n='a';
		switch(n) {
		case 'a':
			System.out.println("apple");
		break;
		case 'b':
			System.out.println("boy");
		break;
		case 'c':
			System.out.println("cat");
		break;
		default:
			System.out.println("Exit");
		break;
		}
	}

}
